uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case MT(MOD_LCTL, KC_A):
            return TAPPING_TERM + 10;
        case LT(1, KC_ENTER):
            return TAPPING_TERM -30;
        case MT(MOD_RCTL, KC_SCLN):
            return TAPPING_TERM + 10;
        case LT(2, KC_BSPC):
            return TAPPING_TERM -30;
        default:
            return TAPPING_TERM;
    }
}


