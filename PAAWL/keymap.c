uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case MT(MOD_LSFT, KC_S):
            return TAPPING_TERM -5;
        case LT(1, KC_ENTER):
            return TAPPING_TERM -50;
        case MT(MOD_RSFT, KC_L):
            return TAPPING_TERM + 15;
        case LT(2, KC_BSPC):
            return TAPPING_TERM -50;
        default:
            return TAPPING_TERM;
    }
}


